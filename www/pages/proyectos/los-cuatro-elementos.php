    <p class="quote text-2xl uppercase">Los cuatro elementos</p>
    <p class="font-bold text-2xl"><em>«</em>Estamos viviendo en una época en la que el conocimiento de la tierra y de la mujer está relacionado con la conciencia colectiva<em>»</em></p>
    <p class="font-bold">Lara Owen</p>
    <p>Esta asociación, aunque perdida en nuestra sociedad, se ha dado desde el principio de tiempos. Así los griegos ya consideraban a Gea (Tellus para los romanos) la principal dei­dad femenina, símbolo de fertilidad, bajo el concepto de la Tierra como Diosa Madre, personificación que se repite en distintas culturas ancestrales. De este modo, cuerpo y naturaleza se unen en lo femenino como vínculo con el origen del mundo, al generar una conexión esencial que se relaciona de manera directa con la Tierra, la Luna y sus ritmos.</p>
    <p>El cambio climático, la contaminación, la escasez de agua, la extinción de especies y la pérdida de biodiversidad, el consumo cada vez más elevado de energía o la generación de toneladas de basura y residuos son solo algunos de los problemas ambientales a los que nos enfrentamos en el siglo XXI. Como artista sensible a esta crisis medioambiental, me aproximo a ella con esta serie que pretende ahondar y acentuar los vínculos entre los elementos y ciclos naturales y el cuerpo de la mujer.</p>
</div>

