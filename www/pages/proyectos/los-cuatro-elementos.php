<div class="max-w-4xl mx-auto px-5 py-12 prosed">
    <p>Cada pieza, ya sea de forma visual o textual, constituye un reencuentro con la naturaleza y su valor, una invitación a habitar y relacionarnos con el entorno de una forma más consciente y respetuosa. Cada obra pretende aportar una visión integradora, en la que subrayo la necesidad de abandonar la visión antropocéntrica, entendiendo que no somos el punto central del planeta, sino que formamos parte del mismo y somos tan vulnerables como él. La serie es un llamado a reconocer la importancia de la búsqueda de equilibrio y armonía con nuestro entorno. De ahí la ausencia de rostros que se diluyen a través de las pinceladas de acrílico la luz o la perspectiva.</p>
    <p>La mujer articula estos paisajes como muestra de las potencialidades femeninas que representa una reconciliación con lo natural, con el poder de nutrir la vida por medio de su carácter creador y creativo.</p>
    <p>Por su parte, la naturaleza se convierte en estas obras en inspiración y objeto artístico, con el ánimo de iniciar un diálogo entre medio ambiente y arte. De ahí, la doble vertiente, ambas poéticas, donde adquiere protagonismo o bien la imagen o bien la palabra.</p>
