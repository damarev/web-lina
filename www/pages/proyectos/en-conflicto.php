    <p class="quote text-2xl uppercase">En conflicto</p>
    <p>El 7 de octubre marcó el inicio de cobertura masiva sobre una agresión brutal contra la población civil, aunque esta y otras ya acontecían, porque la guerra siempre nos acompaña, aunque sea en silencio. Desde entonces, presenciamos una escalada de violencia extrema, que vulnera los derechos humanos más fundamentales. Mientras el genocidio sucede, quienes alzan su voz contra la injusticia se enfrentan a la censura, al acoso y hasta la muerte. Como evidencia tenemos, entre otras, el asesinato, en 3 meses, de más de 100 trabajadores humanitarios en Gaza, el mayor número en un conflicto en la historia de la ONU, según datos extraídos del Informe de la situación en Gaza del 8 de noviembre de 2023 por UNRWA España.</p>
    <p>La crudeza de esta realidad, normalizada por la exposición constante a la violencia, deshumaniza a quienes están detrás de las cifras. <em>En conflicto</em> surge para explorar las consecuencias visibles y simbólicas de la guerra y la barbarie. El proyecto, centrado en una serie de palabras intervenidas, o más bien dañadas, desafía la insensibilidad al recordarnos que detrás de cada estadística hay vidas, historias y la urgente necesidad de recuperar y preservar nuestra humanidad en medio de una emergencia global. Porque tal vez no solo nos enfrentemos a una crisis humanitaria, sino también a una crisis de humanidad.</p>
