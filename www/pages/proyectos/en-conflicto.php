<div class="max-w-4xl mx-auto px-5 py-12 prosed">
    <p>Este proyecto parte de una reflexión profunda sobre los derechos fundamentales y las secuelas implacables de la guerra. Su origen emerge de la necesidad visceral de explorar la esencia misma de la humanidad en un entorno de máxima violencia, poniendo especial atención en las consecuencias devastadoras que los crímenes de guerra infligen no solo en el tejido social, sino también en los pilares mismos que sostienen nuestra humanidad.</p>
    <p>La crueldad en Gaza, difundida a gran escala desde el pasado octubre, ha sido el detonante a partir del cual he creado una serie de obras, resultado de una performance. Estas piezas trascienden las fronteras geográficas para abordar las consecuencias humanitarias universales de la violencia. <em>En conflicto</em> desciende hasta la esencia misma de los valores fundamentales que definen la humanidad, donde cada pieza destaca un derecho básico universal, como el hogar, la justicia, la empatía, la solidaridad o la compasión. De este modo, este trabajo se articula a partir de una serie de palabras que representan derechos básicos y se convierten en actores principales, al ser grapados como acto simbólico. Esta intervención, lejos de ser un acto de afirmación, es el prólogo de una narrativa visual que se despliega a través de una acción destructora.</p>
    <p>La performance se centra en la agresión deliberada de grapar esos conceptos hasta dejarlos prácticamente ilegibles para, después, extraer estas grapas con dureza, grabando tras de sí huellas visuales que, como cicatrices imborrables, imprimen marcas sobre esas palabras para recordarnos que son, además, derechos. Un gesto que simboliza la brutalidad de la guerra y sus efectos desgarradores. Cada palabra queda marcada y su integridad comprometida, dejando señales que reflejan la crudeza de la violencia y sus consecuencias en nuestro sistema de valores. El acto que adquiere todo el sentido cuando las grapas extraídas son guardadas en botes con nombres de lugares civiles bombardeados, como escuelas, hospitales o campos de refugiados.</p>
    <p>Esta manipulación sobre conceptos como hogar (home), empatía (empathy) o esperanza (hope)… revelan daños que representan la pérdida de refugio y seguridad y la fractura de los lazos humanos en medio de situaciones tremendamente hostiles.</p>
</div>

