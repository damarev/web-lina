    <p class="quote text-2xl uppercase">5 cm/s</p>
    <p class="font-bold text-2xl"><em>«</em>Viviendo únicamente para uno mismo, la tristeza se va acumulando... en las hojas puestas al sol para que se sequen, en el cepillo de dientes... en el registro de las llamadas del móvil.<em>»</em></p>
    <p class="font-bold"><em>5 centímetros por segundo</em>, Makoto Shinkai</p>
    <p>En 2020, después de meses de confinamiento provocado por la crisis sanitaria de la COVID-19, sentí la ciudad como un espacio vivo, donde la naturaleza había florecido de manera extraordinaria. Su capacidad inherente de generar vida supuso un soplo de esperanza. Mensaje que quise compartir sustituyendo el papel, soporte habitual de mi trabajo, por la calle a través de una <a href="https://www.instagram.com/p/B_97C0BKsIU/" target="_blank">intervención urbana: <em>Las flores y el arte también curan</em></a>. Una forma, además, de poner en valor el poder de la naturaleza, el arte y la cultura.</p>
    <p>A partir de ese momento, empecé a desarrollar este proyecto. Un reto en el que pasé de utilizar el papel como soporte plano, a componer con volumen. De este modo, utilizando como material de trabajo noticias impresas relacionadas con la pandemia y seleccionadas desde el inicio de la misma, creé una instalación floral en papel, que simboliza nuestra capacidad de resiliencia, un año después de esa intervención urbana que realicé con flores recogidas durante uno de los primeros paseos permitidos en mayo 2020.</p>
</div>

