<div class="max-w-4xl mx-auto px-5 py-12 prosed">
    <p>Esta intervención, que realicé durante la primavera de 2020, es el punto de partida de <em>5 cm/s</em>. Título inspirado en la película homónima de Makoto Shinkai que indica la velocidad a la cual los pétalos de cerezo caen del árbol; siendo estos una representación metafórica de las personas, el paso del tiempo y la distancia. La naturaleza se convierte así en el hilo conductor de este proyecto, donde reflexiono sobre las relaciones humanas, y el ciclo de la vida. De este modo, canalizo el sentir colectivo y la experiencia individual vividos durante la pandemia con el fin de acercarnos a esta experiencia desde una perspectiva regeneradora y casi catártica para ahondar sobre el trauma y el dolor.</p>
    <p>Durante dos años recogí de manera meticulosa noticias sobre la COVID-19. Estas, gracias al asesoramiento y apoyo de Carmen Ledesma, fundadora del <a href="https://www.nikuafloral.es/" target="_blank">taller de botánica artesanal Níkua</a>, fueron las hojas y pétalos que dan vida a las flores de la instalación. Todas ellas están realizadas a mano. Cada hoja, pétalo, pistilo y tallo se ha realizado de manera particular. Cada elemento ha sido recortado, pintado, moldeado y pegado de manera artesanal. Por lo que cada una de ellas es única, como cada una de las experiencias vividas durante ese período.</p>
    <p>Las ramas donde se ensamblan son naturales, únicamente han sido podadas para ajustarse al diseño ideado. Fueron recuperadas de los estragos producidos tras el temporal Filomena, en homenaje al ramillete recogido de la calle, génesis del proyecto.</p>
</div>

