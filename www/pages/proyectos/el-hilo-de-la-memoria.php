<div class="max-w-4xl mx-auto px-5 py-12 prosed">
    <p>La instalación no solo es un testimonio del proceso de transformación artística, sino también un recordatorio de la tradición arraigada en las antiguas prácticas domésticas, que fueron sustento durante décadas en Santa María del Berrocal y el valle de la Corneja. Cada detalle revela el cuidado y la dedicación en la preservación y la conexión con el pasado.</p>
    <p>Las tiras de periódico, que en su origen servían como embalaje y protección, rescatan y destacan el trabajo muchas veces subestimado de las mujeres, asociado al cuidado y que ha sido fundamental para el desarrollo económico y social del pueblo. Estos hilos de papel se entrelazan con el textil en un acto de hilvanar que no solo rememora el trabajo manual, sino el proceso de investigación, realizado a través entrevistas a las vecinas y vecinos del pueblo. Testimonios y memoria convergen en esta obra, que busca dejar constancia de la valiosa contribución de las mujeres.</p>
</div>

