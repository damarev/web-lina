    <p>Este libro collage habla sobre la paz en un contexto internacional de guerra. Con este proyecto artístico, encapsulado en este libro de artista, utilizo un poemario de amor como lienzo narrativo, fusionando la riqueza de la expresión poética con una profunda meditación sobre el enfrentamiento bélico. ¿Acaso nuestro dolor no refleja el del mundo? ¿En qué momento creímos que podíamos vivir al margen de lo que sucede a nuestro alrededor?</p>
    <p>La elección de un poemario recuperado no es casual, ya que conecta con nuestros conflictos internos, como la pérdida, la frustración o la tristeza. De esta forma, la vulnerabilidad se convierte en el eje vertebrador de esta pieza, donde la fragilidad, al igual que el arte, se presenta como un medio que favorece la reparación y transformación hacia la paz, la alegría y el amor.</p>
