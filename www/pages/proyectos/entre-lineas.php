<div class="max-w-4xl mx-auto px-5 py-12 prosed">
    <p>Formalmente <em>Entre líneas</em> se trata de una serie de noticias intervenidas. En cada pieza analizo, reconstruyo y resignifico fragmentos periodísticos, a través de una lectura por capas, donde prescindo de lo accesorio para extraer un nuevo significado, dándoles una transcendencia diferente. De este modo, vuelvo a dotar a la lectura de un acto íntimo y profundo, de ahí el pequeño formato de la mayoría de las piezas.</p>
    <p>Con esta «re-lectura» elimino la información sobrante para desvelar un nuevo texto que el original ya contenía, haciendo referencia a Miguel Ángel cuando hablaba de su trabajo escultórico, del cual decía que «la escultura ya estaba dentro de la piedra».</p>
    <p>La selección de los textos que intervengo es totalmente azarosa. Siempre trabajo a partir de medios impresos físicos, no digitales, encontrados o cedidos, nunca comprados, como una forma de reflexión en torno el acceso a la información y el tiempo de exposición a las pantallas.</p>
    <p>Lo aleatorio, junto con la fragmentación, son elementos relevantes en mi obra, ya que es a partir de estos como reconstruyo un nuevo escrito y genero nuevos significados. Estos son esenciales también en el collage, técnica con la que trabajo habitualmente y que, a su vez, hace alusión a su manifestación cubista, en la que los artistas unían realidades diversas para alterar la noción de verdad preconcebida por el espectador.</p>
</div>

